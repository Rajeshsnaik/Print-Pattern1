package Patterns;
public class a5{
    public static void main(String args[])
    {
        int n=4;
        int m=5;
        for (int i=n;i>=1;i--)
        {
           for (int j=i;j<=m;j++)
           
           {
                System.out.print("*");
           }
           System.out.println();
        }
    }

}
